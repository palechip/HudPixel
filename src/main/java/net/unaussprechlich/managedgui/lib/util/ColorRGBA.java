public class ColorRGBA {

    public float RED = 0;
    public float GREEN = 0;
    public float BLUE = 0;
    public float ALPHA = 0;

    public ColorRGBA() {}

    public ColorRGBA(float RED, float GREEN, float BLUE, float ALPHA) {
        this.RED = RED;
        this.GREEN = GREEN;
        this.BLUE = BLUE;
        this.ALPHA = ALPHA;
    }
}
