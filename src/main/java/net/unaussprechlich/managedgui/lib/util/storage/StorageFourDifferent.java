public class StorageFourDifferent<T, E, U, V> {

    public T t;
    public E e;
    public U u;
    public V v;


    public StorageFourDifferent(T t, E e, U u, V v) {
        this.t = t;
        this.e = e;
        this.u = u;
        this.v = v;
    }
}
